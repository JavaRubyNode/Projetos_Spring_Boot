package com.vinicius.sangue.enums;

public enum TipoSanguineo {

	A_negativo("A-"),
	A_positivo("A+"),
	B_negativo("B-"),
	B_positivo("B+"),
	AB_negativo("AB-"),
	AB_positivo("AB+"),
	O_negativo("O-"),
	O_positivo("O+");
	
	
	private String sangue;
	
	TipoSanguineo(String sangue){
		this.sangue=sangue;
	}
	
	public String getTipoSangue(){
		return sangue;
	}
	
}
